# Example 1: Create and add/remove
fruits = {"apple", "banana"}
fruits.add("cherry")
fruits.remove("banana")

# Example 2: Membership and uniqueness
nums = [1, 2, 2, 3, 3, 3]
unique = set(nums)
print(unique)  # {1, 2, 3}

# Example 3: Set operations
a = {1, 2, 3}
b = {3, 4, 5}
print(a & b)  # intersection {3}
print(a | b)  # union {1,2,3,4,5}

