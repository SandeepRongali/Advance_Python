# Example 1: Concatenation and repetition
hello = "Hello"
world = "World"
print(hello + " " + world)
print("Ha" * 3)

# Example 2: Slicing
s = "Python"
print(s[1:4])
print(s[:2])
print(s[-2:])

# Example 3: Searching and replacing
quote = "To be or not to be"
print("be" in quote)
print(quote.replace("be", "eat"))
