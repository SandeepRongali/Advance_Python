# Example 1: Basic f‑string
name = "Alice"
age = 30
print(f"{name} is {age} years old.")

# Example 2: Expressions inside f‑strings
a, b = 5, 3
print(f"{a} + {b} = {a + b}")

# Example 3: Formatting numbers
pi = 3.14159265
print(f"Pi is approximately {pi:.2f}")  # Pi is approximately 3.14
